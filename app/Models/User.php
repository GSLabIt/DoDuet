class User extends Authenticatable implements HasMedia, MustVerifyEmail
{
    use HasApiTokens, HasFactory, HasProfilePhoto, Notifiable, TwoFactorAuthenticatable, InteractsWithMedia;
    use LogsActivity, Uuid, ActivityLogAll, CausesActivity, HasRoles;

    public $connection = "sso";

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name',
        'email',
        'password',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password',
        'remember_token',
        'two_factor_recovery_codes',
        'two_factor_secret',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array
     */
    protected $appends = [
        'profile_photo_url',
    ];

    function tracks(): HasMany
    {
        return $this->hasMany(Track::class, "owner_id");
    }

    function favourite(): BelongsToMany
    {
        return $this->belongsToMany(Track::class);
    }

    function votes(): HasMany
    {
        return $this->hasMany(Votes::class, "sender_id");
    }
}
